// Função para limpar CNPJ (remover formatação)
function limparCNPJ(cnpj: string): string {
  return cnpj.replace(/\D/g, '');
}

async function main() {
  try {
    console.log('🔍 Comparando CNPJs da lista com a base de clientes...\n');
    console.log(`Total de CNPJs na lista: ${cnpjsLista.length}\n`);

    // Limpar CNPJs da lista
    const cnpjsLimpos = cnpjsLista.map(item => ({
      ...item,
      cnpjLimpo: limparCNPJ(item.cnpj)
    }));

    // Buscar todos os CNPJs da base
    const rows = await executeQuery<any>(
      'SELECT id, cnpj_limpo, razao_social FROM clientes WHERE cnpj_limpo IS NOT NULL AND cnpj_limpo != ""'
    );

    console.log(`Total de clientes na base: ${rows.length}\n`);

    // Criar um Set com os CNPJs limpos da base para busca rápida
    const cnpjsBase = new Set(rows.map((row: any) => row.cnpj_limpo));

    // Encontrar CNPJs não encontrados
    const cnpjsNaoEncontrados: Array<{ cnpj: string; cnpjLimpo: string; razaoSocial: string }> = [];
    const cnpjsEncontrados: Array<{ cnpj: string; cnpjLimpo: string; razaoSocial: string; idBase: string; razaoSocialBase: string }> = [];

    for (const item of cnpjsLimpos) {
      if (cnpjsBase.has(item.cnpjLimpo)) {
        const clienteBase = rows.find((r: any) => r.cnpj_limpo === item.cnpjLimpo);
        cnpjsEncontrados.push({
          cnpj: item.cnpj,
          cnpjLimpo: item.cnpjLimpo,
          razaoSocial: item.razaoSocial,
          idBase: clienteBase.id,
          razaoSocialBase: clienteBase.razao_social || ''
        });
      } else {
        cnpjsNaoEncontrados.push({
          cnpj: item.cnpj,
          cnpjLimpo: item.cnpjLimpo,
          razaoSocial: item.razaoSocial
        });
      }
    }

    // Exibir resultados
    console.log('='.repeat(80));
    console.log('📊 RESULTADOS DA COMPARAÇÃO');
    console.log('='.repeat(80));
    console.log(`\n✅ CNPJs encontrados na base: ${cnpjsEncontrados.length}`);
    console.log(`❌ CNPJs NÃO encontrados na base: ${cnpjsNaoEncontrados.length}\n`);

    // Salvar CNPJs não encontrados em arquivo
    const outputPath = path.join(__dirname, '../cnpjs-nao-encontrados.txt');
    const outputContent = cnpjsNaoEncontrados.map(item => 
      `${item.cnpj}\t${item.razaoSocial}`
    ).join('\n');

    fs.writeFileSync(outputPath, outputContent, 'utf-8');
    console.log(`📄 Arquivo salvo em: ${outputPath}\n`);

    // Exibir primeiros 20 CNPJs não encontrados
    console.log('='.repeat(80));
    console.log('❌ CNPJs NÃO ENCONTRADOS NA BASE (primeiros 20):');
    console.log('='.repeat(80));
    cnpjsNaoEncontrados.slice(0, 20).forEach((item, index) => {
      console.log(`${(index + 1).toString().padStart(3, ' ')}. ${item.cnpj.padEnd(20, ' ')} - ${item.razaoSocial}`);
    });

    if (cnpjsNaoEncontrados.length > 20) {
      console.log(`\n... e mais ${cnpjsNaoEncontrados.length - 20} CNPJs (ver arquivo completo)`);
    }

    // Exibir alguns CNPJs encontrados como exemplo
    if (cnpjsEncontrados.length > 0) {
      console.log('\n' + '='.repeat(80));
      console.log('✅ EXEMPLOS DE CNPJs ENCONTRADOS (primeiros 5):');
      console.log('='.repeat(80));
      cnpjsEncontrados.slice(0, 5).forEach((item, index) => {
        console.log(`${(index + 1).toString().padStart(3, ' ')}. ${item.cnpj.padEnd(20, ' ')} - ${item.razaoSocial}`);
        console.log(`     Base: ID ${item.idBase} - ${item.razaoSocialBase}`);
      });
    }

    console.log('\n' + '='.repeat(80));
    console.log('✅ Processo concluído!');
    console.log('='.repeat(80));

  } catch (error) {
    console.error('❌ Erro ao processar:', error);
    throw error;
  }
}

// Executar
main().catch(console.error);

